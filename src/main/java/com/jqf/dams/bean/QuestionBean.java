package com.jqf.dams.bean;

public class QuestionBean {

    private String id;

    private String questionId;

    private String questionNumber;

    private String questionTitle;

    private String isMoreSelect;

    private String isMoreSelectDisplay;

    private String answers;

    private String isRelease;

    private String isReleaseDisplay;

    public String getId() {
        return id;
    }

    public void setId(String id) {
        this.id = id;
    }

    public String getQuestionId() {
        return questionId;
    }

    public void setQuestionId(String questionId) {
        this.questionId = questionId;
    }

    public String getQuestionNumber() {
        return questionNumber;
    }

    public void setQuestionNumber(String questionNumber) {
        this.questionNumber = questionNumber;
    }

    public String getQuestionTitle() {
        return questionTitle;
    }

    public void setQuestionTitle(String questionTitle) {
        this.questionTitle = questionTitle;
    }

    public String getIsMoreSelect() {
        return isMoreSelect;
    }

    public void setIsMoreSelect(String isMoreSelect) {
        this.isMoreSelect = isMoreSelect;
    }

    public String getIsMoreSelectDisplay() {
        return isMoreSelectDisplay;
    }

    public void setIsMoreSelectDisplay(String isMoreSelectDisplay) {
        this.isMoreSelectDisplay = isMoreSelectDisplay;
    }

    public String getAnswers() {
        return answers;
    }

    public void setAnswers(String answers) {
        this.answers = answers;
    }

    public String getIsRelease() {
        return isRelease;
    }

    public void setIsRelease(String isRelease) {
        this.isRelease = isRelease;
    }

    public String getIsReleaseDisplay() {
        return isReleaseDisplay;
    }

    public void setIsReleaseDisplay(String isReleaseDisplay) {
        this.isReleaseDisplay = isReleaseDisplay;
    }
}
