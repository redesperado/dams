package com.jqf.dams.bean;

public class DormitoryBean {

    private int id;

    private String dormBuilding;

    private String dormSex;

    private String dormNumber;

    private String dormCapacity;

    private String dormStudents;

    private String isFull;

    private String remainNumber;

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getDormBuilding() {
        return dormBuilding;
    }

    public void setDormBuilding(String dormBuilding) {
        this.dormBuilding = dormBuilding;
    }

    public String getDormSex() {
        return dormSex;
    }

    public void setDormSex(String dormSex) {
        this.dormSex = dormSex;
    }

    public String getDormNumber() {
        return dormNumber;
    }

    public void setDormNumber(String dormNumber) {
        this.dormNumber = dormNumber;
    }

    public String getDormCapacity() {
        return dormCapacity;
    }

    public void setDormCapacity(String dormCapacity) {
        this.dormCapacity = dormCapacity;
    }

    public String getDormStudents() {
        return dormStudents;
    }

    public void setDormStudents(String dormStudents) {
        this.dormStudents = dormStudents;
    }

    public String getIsFull() {
        return isFull;
    }

    public void setIsFull(String isFull) {
        this.isFull = isFull;
    }

    public String getRemainNumber() {
        return remainNumber;
    }

    public void setRemainNumber(String remainNumber) {
        this.remainNumber = remainNumber;
    }
}
